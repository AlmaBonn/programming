
/* Eine Funktion, um die Fakultaet zu berechnen
 *
 * n darf jede beliebige ganze Zahl sein.
 * Davon wird die Fakultaet berechnet und zurueckgegeben.
 
 * Fuer n <= 0 wird 0 zurueckgegeben. 
 */
long
fakultaet (int n);

/* Berechne den Binomialkoeffizienten n ueber k.
 * Wenn n < 0, dann gebe -1 zurueck.
 * Wenn k < 0 oder k > n, dann gebe 0 zurueck.
 */
long
nueberk (int n, int k);
